// Buat function untuk menghitung rata-rata total nilai dari tiap kelas!
function getAverageScore(avgScore) {
  let totalScore = 0;
  for (let i = 0; i < avgScore.length; i++) {
    totalScore +=
      avgScore[i].score.science +
      avgScore[i].score.social +
      avgScore[i].score.math;
  }
  return (totalScore / avgScore.length).toFixed(2);
}

